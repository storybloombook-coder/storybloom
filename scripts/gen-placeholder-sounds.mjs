// Generates PLACEHOLDER synth audio for every library sound id, so matched
// fx_*/amb_*/voice_* cues are audible end-to-end for testing. Swap these .wav
// files for real CC0 audio (same filenames) later — soundAssets.ts stays valid.
//
// Run: node scripts/gen-placeholder-sounds.mjs
//
// Keep the id lists in sync with src/lib/ai/soundLibrary.ts.

import { mkdirSync, readFileSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SR = 22050;

// Read the id lists straight from soundLibrary.ts so this never drifts from it.
const LIB = readFileSync(join(ROOT, 'src', 'lib', 'ai', 'soundLibrary.ts'), 'utf8');
function extractIds(name) {
  const m = LIB.match(new RegExp(`export const ${name}\\s*=\\s*\\[([\\s\\S]*?)\\]`));
  return m ? [...m[1].matchAll(/'([^']+)'/g)].map((x) => x[1]) : [];
}
const AMBIENT_IDS = extractIds('AMBIENT_IDS');
const EFFECT_IDS = extractIds('EFFECT_IDS');
const VOICE_IDS = extractIds('VOICE_IDS');

function toWav(samples) {
  const n = samples.length;
  const buf = Buffer.alloc(44 + n * 2);
  buf.write('RIFF', 0);
  buf.writeUInt32LE(36 + n * 2, 4);
  buf.write('WAVE', 8);
  buf.write('fmt ', 12);
  buf.writeUInt32LE(16, 16);
  buf.writeUInt16LE(1, 20); // PCM
  buf.writeUInt16LE(1, 22); // mono
  buf.writeUInt32LE(SR, 24);
  buf.writeUInt32LE(SR * 2, 28);
  buf.writeUInt16LE(2, 32);
  buf.writeUInt16LE(16, 34);
  buf.write('data', 36);
  buf.writeUInt32LE(n * 2, 40);
  for (let i = 0; i < n; i++) {
    const s = Math.max(-1, Math.min(1, samples[i]));
    buf.writeInt16LE(Math.round(s * 32767), 44 + i * 2);
  }
  return buf;
}

function fadeEnds(s, k) {
  const n = s.length;
  for (let i = 0; i < k && i < n; i++) {
    const g = i / k;
    s[i] *= g;
    s[n - 1 - i] *= g;
  }
  return s;
}

// A short plucked tone with a noise transient — one per effect, distinct freq.
function effect(freq, dur = 0.45) {
  const n = (SR * dur) | 0;
  const s = new Float32Array(n);
  for (let i = 0; i < n; i++) {
    const t = i / SR;
    s[i] = 0.35 * Math.exp(-6 * t) * Math.sin(2 * Math.PI * freq * t);
  }
  for (let i = 0; i < Math.min(220, n); i++) s[i] += 0.18 * (Math.random() * 2 - 1) * (1 - i / 220);
  return fadeEnds(s, 100);
}

// Gentle 2.5s bed: brown-ish noise + slow oscillation — one per ambient scene.
function ambient(freq, dur = 2.5) {
  const n = (SR * dur) | 0;
  const s = new Float32Array(n);
  let brown = 0;
  for (let i = 0; i < n; i++) {
    const t = i / SR;
    brown = brown * 0.99 + (Math.random() * 2 - 1) * 0.012;
    const lfo = 0.05 * Math.sin(2 * Math.PI * 0.3 * t);
    s[i] = 0.12 * brown + (0.04 + lfo) * Math.sin(2 * Math.PI * freq * t);
  }
  return fadeEnds(s, (SR * 0.12) | 0);
}

// Two-syllable blip — one per voice sting.
function voice(freq, dur = 0.35) {
  const n = (SR * dur) | 0;
  const s = new Float32Array(n);
  const half = n >> 1;
  for (let i = 0; i < n; i++) {
    const t = i / SR;
    const f = i < half ? freq : freq * 1.26;
    const env = Math.sin((Math.PI * (i % half)) / half);
    s[i] = 0.3 * env * Math.sin(2 * Math.PI * f * t);
  }
  return fadeEnds(s, 60);
}

const groups = [
  ['ambient', AMBIENT_IDS, ambient, 90, 18],
  ['effects', EFFECT_IDS, effect, 260, 37],
  ['voices', VOICE_IDS, voice, 240, 45],
];

const entries = [];
for (const [folder, ids, gen, base, step] of groups) {
  const dir = join(ROOT, 'assets', 'sounds', folder);
  mkdirSync(dir, { recursive: true });
  ids.forEach((id, idx) => {
    writeFileSync(join(dir, `${id}.wav`), toWav(gen(base + idx * step)));
    entries.push([id, `sounds/${folder}/${id}.wav`]);
  });
}

const map = entries.map(([id, rel]) => `  '${id}': require('../../../assets/${rel}'),`).join('\n');
const ts = `// AUTO-GENERATED by scripts/gen-placeholder-sounds.mjs — do not edit by hand.
// Placeholder synth clips so matched library sounds are AUDIBLE end-to-end.
// Swap the .wav files under assets/sounds/ for real CC0 audio (same filenames);
// this map stays valid. Metro requires static require() paths, hence the table.

export const SOUND_ASSETS: Record<string, number> = {
${map}
};
`;
mkdirSync(join(ROOT, 'src', 'lib', 'audio'), { recursive: true });
writeFileSync(join(ROOT, 'src', 'lib', 'audio', 'soundAssets.ts'), ts);
console.log(`generated ${entries.length} placeholder clips + src/lib/audio/soundAssets.ts`);
